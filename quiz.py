# -*- coding: utf-8 -*-
"""Qualification quizzes for EB-1A / EB-2 NIW / O-1 / E-2."""
from i18n import t

_SPEC = {
    "eb1a": {"intro_key": "quiz_intro_eb1a", "q_key": "eb1a_questions", "needed": 3, "total": 10},
    "niw":  {"intro_key": "quiz_intro_niw",  "q_key": "niw_questions",  "needed": 3, "total": 3},
    "o1":   {"intro_key": "quiz_intro_o1",   "q_key": "o1_questions",   "needed": 3, "total": 8},
    "e2":   {"intro_key": "quiz_intro_e2",   "q_key": "e2_questions",   "needed": 7, "total": 7},
}

def get_quiz(kind: str, lang: str = "ru"):
    """Return localized quiz config for the given kind and language."""
    spec = _SPEC.get(kind)
    if not spec:
        return None
    return {
        "intro":     t(spec["intro_key"], lang),
        "questions": t(spec["q_key"], lang),
        "needed":    spec["needed"],
        "total":     spec["total"],
    }

def summarize(kind: str, answers: list[bool]) -> tuple[str, bool]:
    """
    Returns (summary_text_in_Russian, qualifies_flag).
    The verdict is authored in Russian; bot.py translates it to the user's
    language via llm.translate() if needed. Keeping verdicts in one language
    keeps the logic deterministic and lets us rely on USCIS numeric facts.
    """
    yes = sum(answers)

    if kind == "eb1a":
        qualifies = yes >= 3
        if yes == 10:
            verdict = (
                f"🏆 *Исключительно сильная база: {yes} из 10 критериев.* "
                "Это очень высокий результат — USCIS требует минимум 3, у вас все. "
                "На таком уровне ключевую роль играет не количество, а качество доказательств: "
                "финальный содержательный анализ USCIS смотрит, подтверждает ли совокупность "
                "материалов вашу принадлежность к узкой верхушке области. "
                "Рекомендую консультацию для проработки стратегии подачи и проверки доказательной базы."
            )
        elif yes >= 6:
            verdict = (
                f"✅ *Хороший результат: {yes} из 10 критериев.* "
                "Минимум USCIS — 3 критерия, у вас значительно больше. "
                "Количество критериев — только первый шаг; USCIS далее оценивает совокупность "
                "доказательств. Рекомендую консультацию для выбора самых сильных критериев "
                "и подготовки доказательной базы."
            )
        elif yes >= 4:
            verdict = (
                f"✅ *Вы соответствуете {yes} из 10 критериев.* "
                "Минимум USCIS — 3, у вас больше. Однако решающее значение имеет не число, "
                "а убедительность доказательств по каждому критерию. "
                "Рекомендую консультацию для оценки силы доказательств и стратегии подачи."
            )
        elif yes == 3:
            verdict = (
                f"⚠️ *Вы соответствуете минимуму: {yes} из 10 критериев.* "
                "Это базовый порог USCIS — ровно минимум. "
                "На этом уровне каждый критерий должен быть подтверждён очень убедительно, "
                "потому что USCIS дополнительно проводит оценку совокупности доказательств. "
                "Рекомендую консультацию — специалист оценит, насколько сильны ваши доказательства "
                "и есть ли смысл параллельно рассмотреть *EB-2 NIW* или *O-1*."
            )
        else:
            verdict = (
                f"⚠️ *Вы соответствуете {yes} из 10 критериев.* "
                "Минимум для EB-1A — 3, этого пока недостаточно. "
                "Возможно, стоит рассмотреть *EB-2 NIW* (меньше формальных требований, "
                "фокус на национальной значимости работы) или *O-1* (временная виза, "
                "схожие критерии, но без финального merits-теста). "
                "Специалист поможет подобрать оптимальный путь."
            )

    elif kind == "niw":
        qualifies = yes == 3
        if qualifies:
            verdict = (
                "✅ *Все три элемента теста Dhanasar выполнены.* "
                "Это хорошая стартовая позиция для EB-2 NIW. "
                "Следующий шаг — документальное подтверждение каждого элемента "
                "и подготовка петиции I-140. Рекомендую консультацию для стратегии кейса."
            )
        elif yes == 2:
            verdict = (
                f"⚠️ *Выполнено {yes} из 3 элементов теста Dhanasar.* "
                "Для NIW требуются *все три* — одного недостающего элемента достаточно для отказа. "
                "Часто недостающий элемент можно выстроить (например, усилить доказательства "
                "национальной значимости или подготовки к продвижению). "
                "Рекомендую консультацию — специалист оценит, реально ли закрыть пробел."
            )
        elif yes == 1:
            verdict = (
                f"⚠️ *Выполнен только 1 из 3 элементов теста Dhanasar.* "
                "Для NIW этого недостаточно — нужны все три. "
                "Возможно, стоит рассмотреть стандартную *EB-2* (с PERM и работодателем) "
                "или другие пути. Специалист поможет подобрать оптимальный вариант."
            )
        else:  # yes == 0
            verdict = (
                "⚠️ *Ни один из трёх элементов теста Dhanasar не выполнен.* "
                "EB-2 NIW вряд ли подходит в текущей ситуации. "
                "Специалист поможет определить, какие иммиграционные пути доступны "
                "именно в вашем случае (EB-3, семейная иммиграция, рабочие визы)."
            )

    elif kind == "o1":
        qualifies = yes >= 3
        if yes == 8:
            verdict = (
                f"🏆 *Очень сильная база: {yes} из 8 критериев O-1A.* "
                "USCIS требует минимум 3, у вас все 8. "
                "Следующий шаг — оформление работодателя-спонсора (O-1 требует петиционера) "
                "и получение консультационного письма от профессиональной организации. "
                "Рекомендую консультацию для подготовки стратегии."
            )
        elif yes >= 5:
            verdict = (
                f"✅ *Хороший результат: {yes} из 8 критериев O-1A.* "
                "Минимум — 3, у вас больше. Нужен работодатель-спонсор и консультационное письмо "
                "от профильной организации. Рекомендую консультацию для подготовки кейса."
            )
        elif yes == 3 or yes == 4:
            verdict = (
                f"✅ *Вы соответствуете минимуму: {yes} из 8 критериев O-1A.* "
                "3 — это порог USCIS. Нужен работодатель-спонсор и консультационное письмо "
                "от профильной организации. Рекомендую консультацию, чтобы оценить силу "
                "доказательств и, возможно, рассмотреть EB-1A или EB-2 NIW параллельно."
            )
        else:
            verdict = (
                f"⚠️ *Вы соответствуете {yes} из 8 критериев O-1A.* "
                "Минимум — 3, этого пока недостаточно. "
                "Возможно, стоит рассмотреть другие визовые категории. "
                "Обсудим на консультации."
            )

    else:  # e2
        qualifies = yes == 7
        # answers[0] = citizenship of treaty country — hard blocker
        has_citizenship = answers[0] if answers else False

        if qualifies:
            verdict = (
                "✅ *Все 7 требований E-2 выполнены.* "
                "Это хорошая стартовая позиция. Следующий шаг — подготовка инвестиционного плана "
                "(business plan на 5 лет), документальное подтверждение источника средств "
                "и проработка «at-risk» инвестиций. Рекомендую консультацию для стратегии кейса."
            )
        elif not has_citizenship:
            verdict = (
                f"❌ *У вас нет гражданства страны с E-2 договором — это жёсткое, "
                f"непреодолимое условие.*\n\n"
                f"Выполнено {yes} из 7 требований E-2, но первое требование (гражданство "
                f"treaty country) не выполнено — без него E-2 недоступна, независимо от других условий.\n\n"
                f"*Альтернативы для вас:*\n"
                f"- *EB-5* — инвестиционная грин-карта, не требует гражданства treaty country "
                f"(минимум $800 000 в TEA).\n"
                f"- *L-1* — если у вас есть бизнес в другой стране и вы переводите сотрудника в США.\n"
                f"- *EB-1A / EB-2 NIW / O-1* — если есть выдающиеся достижения в профессии.\n"
                f"- *E-2 через второе гражданство* — если можно получить паспорт treaty country "
                f"(Молдова, Грузия, Армения и др.).\n\n"
                f"Рекомендую консультацию для подбора оптимального пути."
            )
        elif yes == 6:
            verdict = (
                f"⚠️ *Выполнено {yes} из 7 требований E-2 — не хватает одного.* "
                "Для E-2 нужны все 7. Часто недостающий элемент можно выстроить: например, "
                "увеличить инвестицию, реструктурировать собственность или уточнить «at-risk» статус. "
                "Рекомендую консультацию — специалист разберёт, какое именно требование "
                "недостаёт и как его закрыть."
            )
        else:  # 1-5 yes with citizenship
            verdict = (
                f"⚠️ *Выполнено {yes} из 7 требований E-2.* "
                "Для этой визы нужны все 7. Часть требований (размер инвестиции, структура бизнеса, "
                "контроль) можно выстроить, но нужно понимать, сколько элементов отсутствует "
                "и какие из них принципиально не закрыть. "
                "Рекомендую консультацию — специалист оценит реальность доработки кейса."
            )

    return verdict, qualifies
